const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true
  },
  password: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

// Drop any existing indexes to clean up
userSchema.index({ email: 1 }, { unique: true });

module.exports = mongoose.model('User', userSchema);